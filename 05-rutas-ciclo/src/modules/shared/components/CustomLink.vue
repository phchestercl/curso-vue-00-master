<template>
    <a v-if="isExternalLink" 
        target="_blank"
        :href="link.to">{{link.name}}
    </a>
    <router-link
    v-else
        :to="link.to"
        v-slot="{ href, isActive }"
    >
        <a :href="href"
            :class="isActive?'is-active':'normal-link' "
        >{{link.name}}</a>
    </router-link>
</template>

<script>
export default {
    props:{
        link:{
            type:Object,
            required:true
        }
    },
    computed:{
        isExternalLink(){
            return this.link.to.startsWith('http')
        }
    }
}
</script>

<style scoped>
a{
    text-decoration: none;
}
.is-active{
    color:#42b983
}
.normal-link{
    color: #2c3e50
}

</style>